#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////
//	MACRO DEFINATION
///////////////////////////////////////////////////////////////////////////////////////////////////

#define MAX 100

///////////////////////////////////////////////////////////////////////////////////////////////////
//	ENUM DECLARATION
///////////////////////////////////////////////////////////////////////////////////////////////////

enum {FALSE, TRUE};

///////////////////////////////////////////////////////////////////////////////////////////////////
//	FUNCTION DECLARATION
///////////////////////////////////////////////////////////////////////////////////////////////////

int ArrayComp(int iMax, int *, int *);

///////////////////////////////////////////////////////////////////////////////////////////////////
//	FUNCTION DEFINATION
///////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iMax;
	int iCounter;
	int iArr1[MAX];
	int iArr2[MAX];

	printf("Enter the size of Array's n (< %d) :\t", MAX);
	scanf("%d", &iMax);

	// accepting elements of 1st array and 2nd array

	printf("\n");
	for(iCounter = 0; iCounter < iMax; iCounter++)
	{
		printf("Enter Arr1[%d] element:\t", iCounter);
		scanf("%d", &iArr1[iCounter]);
	}	

	printf("\n");

	for(iCounter = 0; iCounter < iMax; iCounter++)
	{
		printf("Enter Arr2[%d] element:\t", iCounter);
		scanf("%d", &iArr2[iCounter]);
	}

	iMax = ArrayComp(iMax, iArr1, iArr2);

	if(iMax == TRUE)
		printf("\n\t\tBoth Arrays are Equal\n\n");

	else
		printf("\n\t\tBoth Arrays are NOT Equal\n\n");

	return 0;
}

int ArrayComp(int iMax, int *iArr1, int *iArr2)
{
	int iCounter;
	
	for(iCounter = 0; iCounter < iMax; iCounter++)
	{		
		if(iArr1[iCounter] != iArr2[iCounter])
			return FALSE;
	}
	return TRUE;	
}
